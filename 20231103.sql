--테이블 생성 구문
--PERSON 테이블 생성
--	이름  문자열
--  나이  정수
CREATE TABLE PERSON(
	PNAME VARCHAR2(30),
	AGE NUMBER(3)
);

DROP TABLE PERSON;

--학생 테이블
--학번, 이름, 학과명, 평점
CREATE TABLE STUDENT(
	STD_NO CHAR(8) PRIMARY KEY,
	STD_NAME VARCHAR2(30) NOT NULL,
	STD_MAJOR VARCHAR2(50),
	STD_SCORE NUMBER(3,2)
);

DROP TABLE STUDENT;

--회원정보를 저장하는 테이블
--아이디, 비밀번호, 이름, 성별, 나이
CREATE TABLE MEMBER(
	M_ID VARCHAR2(20) PRIMARY KEY,
	M_PASSWD VARCHAR2(30) NOT NULL,
	M_NAME VARCHAR2(30),
	M_AGE NUMBER(3) DEFAULT 1,
	M_GENDER CHAR(1)	
); 

DROP TABLE MEMBER;

--사원정보 저장 테이블
--사원번호, 사원명, 부서번호, 직급번호, 급여, 입사일
CREATE TABLE EMPLOYEE(
	EMP_NO CHAR(6) PRIMARY KEY,
	EMP_NAME VARCHAR2(30),
	DEPT_NO NUMBER(1),
	POS_NO NUMBER(1),
	EMP_SALARY NUMBER(4),
	EMP_JOIN_DATE DATE
);
--부서 정보 저장 테이블
--부서번호, 부서명
CREATE TABLE DEPARTMENT(
	DEPT_NO NUMBER(1) PRIMARY KEY,
	DEPT_NAME VARCHAR2(30)
);
--직급 정보 저장 테이블
--직급번호, 직급명
CREATE TABLE POSITION(
	POS_NO NUMBER(1) PRIMARY KEY,
	POS_NAME VARCHAR2(30)
);
















